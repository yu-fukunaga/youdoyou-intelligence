package generator

import (
	"os"
	"path/filepath"
	"strings"
	"testing"

	"github.com/yu-fukunaga/youdoyou-intelligence/firebase/generator/internal/parser"
)

func newSwiftGen(t *testing.T, schema *parser.Schema) (*SwiftGenerator, string) {
	t.Helper()
	tmpDir := t.TempDir()
	outDir := filepath.Join(tmpDir, "schema")
	if err := os.MkdirAll(outDir, 0750); err != nil {
		t.Fatalf("failed to create outDir: %v", err)
	}
	return NewSwift(schema, outDir), tmpDir
}

func readSwiftFile(t *testing.T, dir, name string) string {
	t.Helper()
	content, err := os.ReadFile(filepath.Join(dir, "schema", name)) // #nosec G304 -- dir is t.TempDir() and name is a hardcoded test filename
	if err != nil {
		t.Fatalf("failed to read %s: %v", name, err)
	}
	return string(content)
}

func TestSwiftGenerate_Header(t *testing.T) {
	schema := &parser.Schema{
		Models: makeModels(map[string]parser.Model{
			"User": {Fields: makeFields(map[string]parser.Field{"name": {Type: "string"}})},
		}),
		Collections: makeCollections(map[string]parser.Collection{
			"users": {Model: "User"},
		}),
	}

	gen, tmpDir := newSwiftGen(t, schema)
	if err := gen.Generate(); err != nil {
		t.Fatalf("Generate failed: %v", err)
	}

	code := readSwiftFile(t, tmpDir, "User.swift")

	if !strings.Contains(code, "// Code generated by youdoyou-firestore-kit. DO NOT EDIT.") {
		t.Error("expected generated code header")
	}
	if !strings.Contains(code, "import FirebaseFirestore") {
		t.Error("expected FirebaseFirestore import")
	}
	if !strings.Contains(code, "import Foundation") {
		t.Error("expected Foundation import")
	}
}

