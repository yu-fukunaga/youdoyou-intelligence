func TestSwiftGenerate_WithSubcollection(t *testing.T) {
	schema := &parser.Schema{
		Models: makeModels(map[string]parser.Model{
			"Thread":  {Fields: makeFields(map[string]parser.Field{"title": {Type: "string"}})},
			"Message": {Fields: makeFields(map[string]parser.Field{"content": {Type: "string"}})},
		}),
		Collections: makeCollections(map[string]parser.Collection{
			"threads": {
				Model: "Thread",
				Path:  "threads/{threadID}",
				Subcollections: makeCollections(map[string]parser.Collection{
					"messages": {Model: "Message", Path: "threads/{threadID}/messages/{messageID}"},
				}),
			},
		}),
	}

	gen, tmpDir := newSwiftGen(t, schema)
	if err := gen.Generate(); err != nil {
		t.Fatalf("Generate failed: %v", err)
	}

	code := readSwiftFile(t, tmpDir, "Thread.swift")

	if !strings.Contains(code, "public enum MessageCollection") {
		t.Error("expected MessageCollection enum for subcollection")
	}
	if !strings.Contains(code, "public static func collectionPath") {
		t.Error("expected collectionPath for subcollection")
	}
	if !strings.Contains(code, "public struct Message") {
		t.Error("expected Message struct in same file")
	}
}

